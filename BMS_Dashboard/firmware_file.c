/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  /* USER CODE BEGIN 2 */
  // 3. Start ADC DMA scanning (Non-blocking)
  if (HAL_ADC_Start_DMA(&hadc1, (uint32_t *)adc1_thermistor_raw, 5) != HAL_OK)
  {
    Error_Handler();
  }
  if (HAL_ADC_Start_DMA(&hadc2, (uint32_t *)adc2_thermistor_raw, 5) != HAL_OK)
  {
    Error_Handler();
  }

  // Force USB Re-enumeration: Pull Low, then High to trigger Laptop discovery
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_2, GPIO_PIN_RESET);
  HAL_Delay(200);
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_2, GPIO_PIN_SET); 
  HAL_Delay(100);

  MX_USB_DEVICE_Init();

  HAL_Delay(100);
  uint8_t clear_faults = 0xFF;
  if (HAL_I2C_Mem_Write(&hi2c1, BQ76930_ADDR, SYS_STAT_REG, 1, &clear_faults, 1, 100) != HAL_OK)
  {
    // Error_Handler(); // Don't block USB
  }

  uint8_t sys_ctrl1_val = 0x10;
  if (HAL_I2C_Mem_Write(&hi2c1, BQ76930_ADDR, SYS_CTRL1_REG, 1, &sys_ctrl1_val, 1, 100) != HAL_OK)
  {
    // Error_Handler();
  }

  uint8_t sys_ctrl2_val = 0x40;
  if (HAL_I2C_Mem_Write(&hi2c1, BQ76930_ADDR, SYS_CTRL2_REG, 1, &sys_ctrl2_val, 1, 100) != HAL_OK)
  {
    // Error_Handler();
  }

  HAL_Delay(250);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE BEGIN WHILE_LOOP_LOGIC */
    uint8_t raw_cell_data[20];
    if (HAL_I2C_Mem_Read(&hi2c1, BQ76930_ADDR, CELL_VOLTAGE_BASE, 1, raw_cell_data, 20, 100) == HAL_OK)
    {
      for (int i = 0; i < 10; i++)
      {
        cell_voltages_raw[i] = ((raw_cell_data[i * 2] & 0x3F) << 8) | raw_cell_data[(i * 2) + 1];
        cell_volts[i] = ((cell_voltages_raw[i] * BQ_ADC_GAIN) / 1000000.0f) + (BQ_ADC_OFFSET / 1000.0f);
      }
    }

    int len = snprintf(data_buffer, sizeof(data_buffer),
              "\r\n--- BMS CELL EMULATOR STATUS ---\r\n"
              "CELL VOLTAGES (V): [%.2f, %.2f, %.2f, %.2f, %.2f, %.2f, %.2f, %.2f, %.2f, %.2f]\r\n"
              "NTC 1-5 (RAW)    : [%u, %u, %u, %u, %u]\r\n"
              "NTC 6-10 (RAW)   : [%u, %u, %u, %u, %u]\r\n",
              cell_volts[0], cell_volts[1], cell_volts[2], cell_volts[3], cell_volts[4],
              cell_volts[5], cell_volts[6], cell_volts[7], cell_volts[8], cell_volts[9],
              adc1_thermistor_raw[0], adc1_thermistor_raw[1], adc1_thermistor_raw[2], adc1_thermistor_raw[3], adc1_thermistor_raw[4],
              adc2_thermistor_raw[0], adc2_thermistor_raw[1], adc2_thermistor_raw[2], adc2_thermistor_raw[3], adc2_thermistor_raw[4]);

    if (len > 0)
    {
      uint16_t tx_len = (len < (int)sizeof(data_buffer)) ? (uint16_t)len : (uint16_t)(sizeof(data_buffer) - 1);
      (void)CDC_Transmit_FS((uint8_t *)data_buffer, tx_len);
    }

    HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_11);
    HAL_Delay(500);

    /* USER CODE END WHILE_LOOP_LOGIC */
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

